export type Phase = 'build' | 'raid' | 'victory' | 'defeat';
export type BlockType = 'wall' | 'trap' | 'turret' | 'frost';
export type RaiderKind = 'grunt' | 'runner' | 'brute';
export type Cell = { x: number; z: number };
export type Block = { type: BlockType; hp: number; cooldown?: number };
export type Raider = {
  id: string;
  kind: RaiderKind;
  cell: Cell;
  hp: number;
  maxHp: number;
  speed: number;
  bounty: number;
  slowed?: number;
  path: Cell[];
  pathIndex: number;
  resolved?: boolean;
  attackCooldown: number;
};
export type Resources = Record<BlockType, number>;
export type GameState = {
  day: number;
  phase: Phase;
  paused: boolean;
  size: number;
  core: Cell;
  coreHp: number;
  maxCoreHp: number;
  resources: Resources;
  selected: BlockType;
  blocks: Record<string, Block>;
  raiders: Raider[];
  totalRaiders: number;
  coins: number;
  kills: number;
  combo: number;
  dangerLane: number;
  message: string;
};
